// Helper function to get services by category
export const getServicesByCategory = (categoryId: string): PriceItem[] => {
  if (categoryId === 'all') {
    return allServices;
  }

  return allServices.filter((service) => service.category === categoryId);
};

// Helper function to get popular services
export const getPopularServices = (categoryId: string): PriceItem[] => {
  if (categoryId === 'all') {
    return allServices.filter((service) => service.popular === true);
  }

  return allServices.filter(
    (service) => service.popular === true && service.category === categoryId
  );
};

// Helper function to get related services (same category, excluding the current service)
export const getRelatedServices = (serviceId: string, limit: number = 3): PriceItem[] => {
  const service = allServices.find((s) => s.id === serviceId);

  if (!service) {
    return [];
  }

  return allServices
    .filter((s) => s.category === service.category && s.id !== serviceId)
    .slice(0, limit);
};

// NEW: Helper function to check if a service requires consultation
export const requiresConsultation = (service: PriceItem, isNewClient: boolean = true): boolean => {
  // If service explicitly includes consultation, it doesn't require a separate one
  if (service.includesConsultation) {
    return false;
  }

  // For hygiene category, consultation requirement depends on client status
  if (service.category === 'hygiene') {
    // For existing clients, regular hygiene doesn't require consultation
    if (!isNewClient && service.id === 'hygiene-regular') {
      return false;
    }

    // For new clients, all hygiene services require consultation
    if (isNewClient) {
      return true;
    }
  }

  // Use the service-specific flag if available
  if (service.requiresConsultation !== undefined) {
    return service.requiresConsultation;
  }

  // Otherwise use the category default
  const category = serviceCategories.find((cat) => cat.id === service.category);
  return category?.requiresConsultation || false;
};
